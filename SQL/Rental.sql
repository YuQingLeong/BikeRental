CREATE Table Rental (
	RentalID INT NOT NULL PRIMARY KEY IDENTITY,
	CustomerID INT NOT NULL,
	BikeID INT NOT NULL,
	rentalDuration DECIMAL(5,2) NOT NULL,
	rentalStartTime DATETIME,
    FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID) ON DELETE CASCADE ON UPDATE NO ACTION,
    FOREIGN KEY (BikeID) REFERENCES Bikes(BikeID) ON DELETE CASCADE ON UPDATE NO ACTION
);

